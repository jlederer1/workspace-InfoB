/**
 * This class contains the main-method of the program.
 * The program itself prints "Hello World" to the terminal
 */
//In Java wird alles in Klassen ausgefuehrt
public class HelloWorld{
	
	//Main Methode, die beim Start von "java HelloWorld" ausgefuehrt wird	
	//Achtung, diese Methode ist static, d.h. sie kann nicht auf die
	//Instanzvariablen der Klasse HelloWorld zugreifen
	public static void main(String[] args){
		//Ausgabe eines Strings auf dem Terminal
		System.out.println("Hello World");
	}
}
